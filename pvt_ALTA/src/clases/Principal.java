    public void bloquear(){
        switch(menuB){//desbloquea el boton anterior
            case "Inicio":
                inicio.setEnabled(true);
                break;
            case "Inventarios":
                inventarios.setEnabled(true);
                break;
            case "Reportes":
                Reportes.setEnabled(true);
                break;
            case "Pedidos":
                Pedidos.setEnabled(true);
                break;
            default:
                break;
        }
        switch(menuA){//bloque le boton dle menu actual
            case "Inicio":
                inicio.setEnabled(false);
                Titulo.setText("Menu Principal");
                break;
            case "Inventarios":
                inventarios.setEnabled(false);
                Titulo.setText("Inventarios");
                break;
            case "Reportes":
                Reportes.setEnabled(false);
                Titulo.setText("Reportes");
                break;
            case "Pedidos":
                Pedidos.setEnabled(false);
                Titulo.setText("Pedidos");
                break;
            default:
                break;
        }
    }
    
    private void inventariosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_inventariosActionPerformed
        menuB=menuA;
        menuA="Inventarios";
        bloquear();
        panel1=new Inventarios();//abriendo panel desde la pagina principal
        panel1.setBounds(0, 0, 630, 540);
        pPanel2.removeAll();//quita lo que se tiene en el panel 
        pPanel2.add(panel1);// agrega el nuevo panel
        pPanel2.revalidate();//sin esta linea no se muestarn los ocmponentes internos de panel
        pPanel2.repaint();// no se muestran lo colores de los componentes del panel
    }//GEN-LAST:event_inventariosActionPerformed

